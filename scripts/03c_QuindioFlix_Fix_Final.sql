-- Conteo de las dos tablas corregidas
SELECT 'REPRODUCCIONES' AS tabla, COUNT(*) AS registros FROM REPRODUCCIONES
UNION ALL
SELECT 'CALIFICACIONES', COUNT(*) FROM CALIFICACIONES;

-- Reproducciones por partición (evidencia del particionamiento)
SELECT partition_name       AS particion,
       num_rows             AS reproducciones,
       tablespace_name      AS tablespace_fisico
FROM   user_tab_partitions
WHERE  table_name = 'REPRODUCCIONES'
ORDER BY partition_position;

-- Reproducciones por año y dispositivo (asimetría de datos)
SELECT EXTRACT(YEAR FROM fecha_inicio) AS anio,
       dispositivo,
       COUNT(*)                        AS total
FROM   REPRODUCCIONES
GROUP  BY EXTRACT(YEAR FROM fecha_inicio), dispositivo
ORDER  BY anio, dispositivo;

-- Calificaciones por estrellas
SELECT estrellas,
       COUNT(*) AS cantidad
FROM   CALIFICACIONES
GROUP  BY estrellas
ORDER  BY estrellas;

