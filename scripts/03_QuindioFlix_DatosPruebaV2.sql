-- ============================================================================
-- SECCIÓN 10: PAGOS (80 registros con PL/SQL)
-- ============================================================================
DECLARE
    v_metodos  SYS.ODCIVARCHAR2LIST := SYS.ODCIVARCHAR2LIST('TC','TD','PSE','Nequi','Daviplata');
    v_pago_id  NUMBER := 1;
    v_estado   VARCHAR2(15);
    v_fecha    DATE;
    v_precio   NUMBER;
BEGIN
    FOR u IN (SELECT u.id_usuario, u.id_plan, p.precio_mensual
              FROM USUARIOS u JOIN PLANES p ON u.id_plan = p.id_plan
              ORDER BY u.id_usuario)
    LOOP
        FOR mes IN 1..CASE WHEN MOD(u.id_usuario,3)=0 THEN 2 ELSE 3 END LOOP

            v_fecha := ADD_MONTHS(DATE '2026-04-10', -mes);

            IF u.id_usuario IN (3,13,22) AND mes = 1 THEN
                v_estado := 'FALLIDO';
            ELSIF MOD(u.id_usuario + mes, 10) = 0 THEN
                v_estado := 'REEMBOLSADO';
            ELSIF MOD(u.id_usuario + mes, 7) = 0 THEN
                v_estado := 'FALLIDO';
            ELSIF MOD(u.id_usuario + mes, 15) = 0 THEN
                v_estado := 'PENDIENTE';
            ELSE
                v_estado := 'EXITOSO';
            END IF;

            INSERT INTO PAGOS VALUES (
                v_pago_id,
                u.id_usuario,
                v_fecha,
                u.precio_mensual,
                v_metodos(MOD(u.id_usuario + mes, 5) + 1),
                v_estado,
                EXTRACT(MONTH FROM v_fecha),
                EXTRACT(YEAR  FROM v_fecha)
            );
            v_pago_id := v_pago_id + 1;
        END LOOP;
    END LOOP;
    COMMIT;
    DBMS_OUTPUT.PUT_LINE('Pagos insertados: ' || (v_pago_id - 1));
END;
