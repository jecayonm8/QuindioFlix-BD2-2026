-- ============================================================================
-- SECCIÓN 11: REPRODUCCIONES (200 registros con PL/SQL)
-- ============================================================================
-- Distribuidas: ~60 en 2024, ~80 en 2025, ~60 en 2026
-- Demuestra el particionamiento de la tabla por fecha

DECLARE
    v_dispositivos SYS.ODCIVARCHAR2LIST := SYS.ODCIVARCHAR2LIST('celular','tablet','TV','computador');
    v_perfiles     SYS.ODCINNUMBERLIST  := SYS.ODCINNUMBERLIST(
                       1,2,3,6,8,9,11,13,14,15,16,17,19,20,22,23,
                       24,26,29,30,31,32,33,34,36,38,39,40,43,44,
                       45,46,47,49,52,54,55,8,9,15,16,32,38,43,1,15,32);
    v_id_rep   NUMBER := 1;
    v_perf     NUMBER;
    v_cont     NUMBER;
    v_ep       NUMBER;
    v_fecha    TIMESTAMP;
    v_fin      TIMESTAMP;
    v_disp     VARCHAR2(15);
    v_porc     NUMBER;
    v_dur      NUMBER;
BEGIN
    FOR i IN 1..200 LOOP
        v_perf := v_perfiles(MOD(i-1, v_perfiles.COUNT) + 1);
        v_cont := MOD(i * 7 + 3, 40) + 1;
        v_disp := v_dispositivos(MOD(i, 4) + 1);
        v_porc := CASE MOD(i, 5)
                    WHEN 0 THEN 100
                    WHEN 1 THEN MOD(i * 3, 20) + 80
                    WHEN 2 THEN MOD(i * 3, 30) + 50
                    WHEN 3 THEN MOD(i * 3, 30) + 20
                    ELSE        MOD(i * 3, 20) + 1
                  END;

        -- Distribuir en 3 años para demostrar particionamiento
        v_fecha := CASE
            WHEN i <= 60  THEN TIMESTAMP '2024-01-15 08:00:00' + NUMTODSINTERVAL(MOD(i*73,330), 'DAY')
            WHEN i <= 140 THEN TIMESTAMP '2025-01-10 09:00:00' + NUMTODSINTERVAL(MOD(i*59,350), 'DAY')
            ELSE               TIMESTAMP '2026-01-05 10:00:00' + NUMTODSINTERVAL(MOD(i*41,100), 'DAY')
        END;

        SELECT duracion_min INTO v_dur FROM CONTENIDO WHERE id_contenido = v_cont;
        v_fin := v_fecha + NUMTODSINTERVAL(ROUND(v_dur * v_porc / 100.0), 'MINUTE');

        -- Episodio para series y podcasts con episodios
        v_ep := NULL;
        IF    v_cont = 11 THEN v_ep := MOD(i*3,16) + 1;
        ELSIF v_cont = 12 THEN v_ep := MOD(i*3,10) + 17;
        ELSIF v_cont = 13 THEN v_ep := MOD(i*3,12) + 27;
        ELSIF v_cont = 15 THEN v_ep := MOD(i*3, 9) + 39;
        ELSIF v_cont = 36 THEN v_ep := 48;
        ELSIF v_cont = 37 THEN v_ep := 49;
        ELSIF v_cont = 38 THEN v_ep := 50;
        END IF;

        INSERT INTO REPRODUCCIONES VALUES (
            v_id_rep, v_perf, v_cont, v_ep,
            v_fecha, v_fin, v_disp, v_porc
        );
        v_id_rep := v_id_rep + 1;
    END LOOP;
    COMMIT;
    DBMS_OUTPUT.PUT_LINE('Reproducciones insertadas: ' || (v_id_rep - 1));
END;
