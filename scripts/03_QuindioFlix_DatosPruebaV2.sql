        'La fotografía es espectacular, muy bien hecha.',
        NULL,
        NULL
    );
BEGIN
    -- GROUP BY garantiza pares únicos (evita ORA-00001 en UNIQUE constraint)
    FOR r IN (
        SELECT id_perfil, id_contenido,
               ROW_NUMBER() OVER (ORDER BY id_perfil, id_contenido) rn
        FROM (
            SELECT DISTINCT id_perfil, id_contenido
            FROM REPRODUCCIONES
            WHERE porcentaje_avance >= 50
              AND id_perfil NOT IN (4,5,27,28,51)
        )
        FETCH FIRST 60 ROWS ONLY
    ) LOOP
        v_estrellas := MOD(r.rn, 5) + 1;
        INSERT INTO CALIFICACIONES VALUES (
            v_cal_id,
            r.id_perfil,
            r.id_contenido,
            v_estrellas,
            v_resenas(MOD(r.rn - 1, v_resenas.COUNT) + 1),
            SYSDATE - MOD(r.rn * 7, 90)
        );
        v_cal_id := v_cal_id + 1;
    END LOOP;
    COMMIT;
    DBMS_OUTPUT.PUT_LINE('Calificaciones insertadas: ' || (v_cal_id - 1));
