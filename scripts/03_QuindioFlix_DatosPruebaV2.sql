-- FIX: Se usa GROUP BY para garantizar pares únicos (id_perfil, id_contenido)
-- Solo para contenido visto >= 50% (regla de negocio RN-05)

DECLARE
    v_cal_id  NUMBER := 1;
    v_estrellas NUMBER;
    v_resenas SYS.ODCIVARCHAR2LIST := SYS.ODCIVARCHAR2LIST(
        'Excelente producción colombiana, muy recomendada.',
        'Buena historia pero el final me decepcionó un poco.',
        'Increíble trabajo del elenco, me encantó cada episodio.',
